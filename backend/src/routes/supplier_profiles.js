/**
 *  @swagger
 *  /api/supplier_profiles:
 *    get:
 *      security:
 *        - bearerAuth: []
 *      tags: [Supplier_profiles]
 *      summary: Get all supplier_profiles
 *      description: Get all supplier_profiles
 *      responses:
 *        200:
 *          description: Supplier_profiles list successfully received
 *          content:
 *            application/json:
 *              schema:
 *                type: array
 *                items:
 *                  $ref: "#/components/schemas/Supplier_profiles"
 *        401:
 *          $ref: "#/components/responses/UnauthorizedError"
 *        404:
 *          description: Data not found
 *        500:
 *          description: Some server error
 */

router.get(
  '/',
  wrapAsync(async (req, res) => {
    const filetype = req.query.filetype;

    const payload = await Supplier_profilesDBApi.findAll(req.query);
    if (filetype && filetype === 'csv') {
      const fields = [
        'id',
        'company_name',
        'contact_name',
        'contact_email',
        'contact_phone',
        'address',
        'certifications',
        'profile_description',

        'rating',
      ];
      const opts = { fields };
      try {
        const csv = parse(payload.rows, opts);
        res.status(200).attachment(csv);
        res.send(csv);
      } catch (err) {
        console.error(err);
      }
    } else {
      res.status(200).send(payload);
    }
  }),
);

